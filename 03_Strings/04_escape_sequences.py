# escape sequence character

story = "My name is Nemo,\nI want to learn programming"   # for a new line
story = "My name is Nemo, want\tto learn programming" # for a tab 
story = "My name is Nemo, \'want to learn programming\'" # for quote
story = "My name is Nemo, \\want to learn programming" # appear a black slash
print(story)